/**
 * 取得簡易姓氏對照表 (向後相容)
 */
export function getSurnameMap(): Record<string, string[]> {
  const result: Record<string, string[]> = {};
  for (const [key, value] of Object.entries(SURNAME_DATA_FULL)) {
    result[key] = value.names;
  }
  return result;
}

/**
 * 取得姓氏詳細資料
 */
export function getSurnameDetail(surname: string): SurnameDetail | null {
  return SURNAME_DATA_FULL[surname] ?? null;
}

